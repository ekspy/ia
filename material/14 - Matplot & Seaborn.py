import matplotlib.pyplot as plt
import numpy as np

x = np.linspace(0, 10, 20)     # 20 pontos entre 0 e 10
y = np.sin(x)                  # seno de x
categorias = ['A', 'B', 'C', 'D']
valores = [5, 7, 3, 4]
dados = np.random.randn(100)   # 100 números aleatórios (distribuição normal)

#  Gráfico de Linha
plt.figure(figsize=(10,6))
plt.subplot(2,3,1)
plt.plot(x, y, marker='o')
plt.title("plot(): Linha")

#  Gráfico de Dispersão
plt.subplot(2,3,2)
plt.scatter(x, y, color="red")
plt.title("scatter(): Dispersão")

#  Gráfico de Barras
plt.subplot(2,3,3)
plt.bar(categorias, valores, color="green")
plt.title("bar(): Barras")

#  Histograma
plt.subplot(2,3,4)
plt.hist(dados, bins=10, color="orange", edgecolor="black")
plt.title("hist(): Histograma")

# Gráfico de Caixa (Boxplot)
plt.subplot(2,3,5)
plt.boxplot(dados)
plt.title("boxplot(): Caixa")

plt.tight_layout()
plt.show()

