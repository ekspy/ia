import seaborn as sns
import matplotlib.pyplot as plt
import pandas as pd

df = pd.DataFrame({
    "Categoria": ["A", "A", "B", "B", "C", "C"],
    "X": [1, 2, 3, 4, 5, 6],
    "Y": [2, 4, 1, 3, 5, 7],
    "Valor": [5, 7, 6, 8, 5, 9]
})

# Dispersão
sns.scatterplot(x="X", y="Y", hue="Categoria", data=df)
plt.title("scatterplot: Dispersão")
plt.show()

# Linha
sns.lineplot(x="X", y="Y", data=df, marker="o")
plt.title("lineplot: Linha")
plt.show()

# Barras
sns.barplot(x="Categoria", y="Valor", data=df)
plt.title("barplot: Barras")
plt.show()

# Histograma
sns.histplot(df["Valor"], bins=5, color="orange")
plt.title("histplot: Histograma")
plt.show()

# Densidade
sns.kdeplot(df["Valor"], fill=True, color="purple")
plt.title("kdeplot: Densidade")
plt.show()

# Boxplot
sns.boxplot(x="Categoria", y="Valor", data=df)
plt.title("boxplot: Caixa")
plt.show()

# Heatmap (correlação)
corr = df.corr(numeric_only=True)
sns.heatmap(corr, annot=True, cmap="coolwarm")
plt.title("heatmap: Mapa de Calor")
plt.show()

# Pairplot
sns.pairplot(df[["X", "Y", "Valor"]], diag_kind="kde")
plt.suptitle("pairplot: Relações entre variáveis", y=1.02)
plt.show()

